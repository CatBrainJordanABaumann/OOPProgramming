#include <iostream>

int main() {
    std::cout << "Hello World" << std::endl;
    
    // std - standard namespace
    // :: - scope resolution operator
    // cout - console output (object of iostream class)
    // << - stream insertion operator
    // endl - end of line

    std::cout << "45 * 98 - 90 = " << 45 * 98 - 90 << std::endl;
    
    return 0;
}